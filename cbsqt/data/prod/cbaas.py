class CBAASFund(Base):

    __tablename__ = 'fund'
    __table_args__ = {'schema': 'cbaas'}
    id = Column(Integer, primary_key=True)
    S_INFO_WINDCODE = Column(String(255))
    fund_name = Column(String(255))
    strategy = Column(Integer)
    strategy_category = Column(Integer)
    asset_class = Column(Integer)
    valid = Column(Integer)
    active_ = Column(Integer)
    fund_status = Column(Integer)
    inception_date = Column(Date)
    latest_nav_date = Column(Date)

    manager_fund_history = relationship('ManagerFundHistory', backref='fund')
    company_fund = relationship('CompanyFund', backref='fund')


class TradeCalendar(Base):

    __tablename__ = 'trade_calendar'
    __table_args__ = {'schema': 'cbaas'}
    id = Column(Integer, primary_key=True)
    cal_end_of_month = Column(Integer)
    date_ = Column(Date)
    exchangs = Column(String(10))
    is_month_end = Column(Binary)
    is_open = Column(Binary)
    prev_trade_date = Column(Date)
    is_quarter_end = Column(Binary)
    sy_wk = Column(String(10))
    is_week_end = Column(Binary)
    is_year_end = Column(Binary)


class FundCompany(Base):

    __tablename__ = 'fund_company'
    __table_args__ = {'schema': 'cbaas'}
    id = Column(Integer, primary_key=True)
    sycamore_firm_id = Column(String(20), nullable=False)
    company_name = Column(String(100))
    company_shortname = Column(String(50))

    company_fund = relationship('CompanyFund', backref='fund_company')
    manager = relationship('Manager', backref='fund_company')
    manager_fund_fistory = relationship('ManagerFundHistory', backref='fund_company')


class CompanyFund(Base):

    __tablename__ = 'company_fund'
    __table_args__ = {'schema': 'cbaas'}
    id = Column(Integer, primary_key=True, autoincrement=True)
    fund_id = Column(Integer, ForeignKey(CBAASFund.id))
    company_id = Column(Integer, ForeignKey(FundCompany.id))


class Manager(Base):

    __tablename__ = 'manager'
    __table_args__ = {'schema': 'cbaas'}
    id = Column(Integer, primary_key=True)
    address1 = Column(String(100))
    address2 = Column(String(100))
    address3 = Column(String(100))
    city = Column(String(50))
    country = Column(String(50))
    email = Column(String(100))
    first_name = Column(String(100))
    investment_span = Column(Float(2))
    job_start_date = Column(Date)
    company_id = Column(Integer, ForeignKey(FundCompany.id))
    wind_manager_id = Column(String(255))
    birthday = Column(Date)

    manager_fund_history = relationship('ManagerFundHistory', backref='manager')


class ManagerFundHistory(Base):

    __tablename__ = 'manager_fund_history'
    __table_args__ = {'schema': 'cbaas'}
    id = Column(Integer, primary_key=True)
    aum = Column(Float(4))
    start_date = Column(Date)
    end_date = Column(Date)
    fund_id = Column(Integer, ForeignKey(CBAASFund.id))
    manager_id = Column(Integer, ForeignKey(Manager.id))
    company_id = Column(Integer, ForeignKey(FundCompany.id))


